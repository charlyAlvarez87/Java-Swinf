    static public Persona conectar(String cedula){
        Persona persona = new Persona();
        try{
            Class.forName(cadenaDriver);
            Connection conexion = DriverManager.getConnection(url, usuario, contrasenha);
            java.sql.Statement st = conexion.createStatement();
            //System.out.println("Cedula = "+persona.getCedula());
            String sentenciaSQL = "SELECT * FROM persona WHERE cedula =\'"+cedula+"\'";
            ResultSet result = st.executeQuery(sentenciaSQL);
                if(result.next()){
                    persona.setIDPersona(Integer.parseInt(result.getString("idpersona")));
                    persona.setNombre(result.getString("nombre"));
                    persona.setCedula(result.getString("cedula"));
                    persona.setSexo(result.getString("sexo"));
                }else
                    persona = null;
                result.close();
                st.close();
                conexion.close();
        }catch(Exception e){
            System.out.println("Error en la conexion"+e.getMessage());
        }
        return persona;
    }
    
